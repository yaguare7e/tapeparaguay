// Apply overrides using dot-notation paths
// ─────────────────────────────────────────────────
function setNestedValue(obj, path, value) {
  const keys = path.split(".");
  let current = obj;
  for (let i = 0; i < keys.length - 1; i++) {
    if (!current[keys[i]]) current[keys[i]] = {};
    current = current[keys[i]];
  }
  current[keys[keys.length - 1]] = value;
}

function deepClone(obj) {
  return JSON.parse(JSON.stringify(obj));
}

// Generate files
const localesToGenerate = Object.keys(overrides);
let count = 0;

for (const locale of localesToGenerate) {
  const localeData = deepClone(base);
  const localeOverrides = overrides[locale];

  for (const [path, value] of Object.entries(localeOverrides)) {
    setNestedValue(localeData, path, value);
  }

  writeFileSync(
    join(messagesDir, `${locale}.json`),
    JSON.stringify(localeData, null, 2) + "\n",
    "utf-8"
  );
  count++;
}

